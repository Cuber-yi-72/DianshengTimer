// 当前语言
let currentLanguage = 'zh-CN';

// 获取翻译文本
function t(key, params = {}) {
    let text = translations[currentLanguage][key] || translations['zh-CN'][key] || key;
    Object.keys(params).forEach(param => {
        text = text.replace(`{${param}}`, params[param]);
    });
    return text;
}

// 切换语言
function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        StorageHelper.setItem(APP_CONFIG.STORAGE_KEYS.LANGUAGE, lang);
        updateAllText();
    }
}

// 更新所有文本
function updateAllText() {
    document.title = t('title');
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (element.tagName === 'INPUT' || element.tagName === 'BUTTON') {
            element.title = t(key);
        } else {
            element.textContent = t(key);
        }
    });

    const headerTitle = document.querySelector('.header-title');
    if (headerTitle) headerTitle.textContent = t('headerTitle');

    const cubeTypeSelect = document.getElementById('cubeTypeSelect');
    if (cubeTypeSelect) {
        cubeTypeSelect.options[0].textContent = t('cornerCube');
        cubeTypeSelect.options[1].textContent = t('twinOctahedron');
        cubeTypeSelect.options[2].textContent = t('cornerOctahedron');
        cubeTypeSelect.options[3].textContent = t('twinOctahedron2x2');
    }

    const prevScrambleBtn = document.getElementById('prevScrambleBtn');
    const nextScrambleBtn = document.getElementById('nextScrambleBtn');
    if (prevScrambleBtn) prevScrambleBtn.textContent = t('prevScramble');
    if (nextScrambleBtn) nextScrambleBtn.textContent = t('nextScramble');

    const copyBtn = document.getElementById('copyBtn');
    const coordinateBtn = document.getElementById('coordinateBtn');
    if (copyBtn) copyBtn.textContent = t('copy');
    if (coordinateBtn) coordinateBtn.textContent = t('coordinate');

    const currentStatsTitle = document.querySelector('.current-stats-card .stats-card-title');
    const bestStatsTitle = document.querySelector('.best-stats-card .stats-card-title');
    if (currentStatsTitle) currentStatsTitle.textContent = t('currentStats');
    if (bestStatsTitle) bestStatsTitle.textContent = t('bestStats');

    const historyTitle = document.querySelector('.history-times-title span');
    if (historyTitle) historyTitle.textContent = t('history');
}

// 初始化语言
function initLanguage() {
    const savedLang = StorageHelper.getItem(APP_CONFIG.STORAGE_KEYS.LANGUAGE, 'zh-CN');
    if (savedLang && translations[savedLang]) {
        currentLanguage = savedLang;
    }
}